CREATE OR REPLACE FUNCTION "public"."get_similar_urls"("project_id" integer) RETURNS TABLE("group_id" integer, "root_url" "text", "similar_urls" "jsonb", "group_size" integer)
    LANGUAGE "sql"
    AS $$
WITH RECURSIVE similar_pairs AS (
  SELECT
    a.id AS url1_id,
    b.id AS url2_id,
    1 - (a.embedding_title_desc <=> b.embedding_title_desc) AS similarity
  FROM urls a
  JOIN urls b ON a.id < b.id
  JOIN sitemaps s1 ON s1.id = a.sitemap_id
  JOIN sitemaps s2 ON s2.id = b.sitemap_id
  WHERE 1 - (a.embedding_title_desc <=> b.embedding_title_desc) > 0.90
  AND s1.project_id = get_similar_urls.project_id  -- Użyj argumentu funkcji
),
recursive_groups AS (
  SELECT url1_id, url2_id FROM similar_pairs
  UNION
  SELECT url2_id, url1_id FROM similar_pairs
),
all_connected AS (
  SELECT
    url1_id AS root_id,
    url2_id AS connected_id
  FROM recursive_groups
  UNION
  SELECT
    ac.root_id,
    rg.url2_id
  FROM all_connected ac
  JOIN recursive_groups rg ON ac.connected_id = rg.url1_id
  WHERE rg.url2_id <> ac.root_id
),
final_groups AS (
  SELECT
    MIN(root_id) AS group_id,
    array_agg(DISTINCT connected_id) AS group_urls
  FROM all_connected
  GROUP BY connected_id
),
distinct_groups AS (
  SELECT
    group_id,
    array_agg(DISTINCT url_id) AS urls_in_group
  FROM (
    SELECT group_id, unnest(group_urls) AS url_id FROM final_groups
  ) sub
  GROUP BY group_id
)
SELECT
  dg.group_id,
  root.url AS root_url,
  json_agg(json_build_object(
    'url_id', u.id,
    'url', u.url,
    'title', u.title,
    'description', u.description,
    'similarity_score', 1 - (root.embedding_title_desc <=> u.embedding_title_desc)
  ) ORDER BY (1 - (root.embedding_title_desc <=> u.embedding_title_desc)) DESC) AS similar_urls,
  COUNT(*) AS group_size
FROM distinct_groups dg
JOIN urls root ON root.id = dg.group_id
JOIN urls u ON u.id = ANY(dg.urls_in_group)
JOIN sitemaps s ON s.id = root.sitemap_id
WHERE s.project_id = get_similar_urls.project_id  -- Użyj argumentu funkcji
GROUP BY dg.group_id, root.url
HAVING COUNT(*) > 1
ORDER BY group_size DESC;
$$;


