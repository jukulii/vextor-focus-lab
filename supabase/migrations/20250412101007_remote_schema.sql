CREATE OR REPLACE FUNCTION "public"."invoke_edge_function"("name" "text", "body" "jsonb", "timeout_milliseconds" integer DEFAULT 10000) RETURNS "void"
    LANGUAGE "plpgsql"
    AS $$
declare
  headers_raw text;
  auth_header text;
  service_key text;
begin
  -- 1. Pobierz nagłówki jeśli wywołanie przyszło z API (PostgREST)
  headers_raw := current_setting('request.headers', true);
  auth_header := case 
    when headers_raw is not null then (headers_raw::json->>'authorization')
    else null 
  end;

  -- 2. Jeśli brak JWT, pobierz klucz usługowy z Vaulta
  if auth_header is null then
    select s.decrypted_secret into service_key 
    from vault.decrypted_secrets as s
    where s.name = 'service_role_key'; 
    
    if service_key is not null then
      auth_header := 'Bearer ' || service_key;
    else
      raise exception 'No JWT or service_role_key found';
    end if;
  end if;

  -- 3. Wywołaj funkcję edge
  perform net.http_post(
    url => util.project_url() || '/functions/v1/' || name,
    headers => jsonb_build_object(
      'Content-Type', 'application/json',
      'Authorization', auth_header
    ),
    body => body,
    timeout_milliseconds => timeout_milliseconds
  );
end;
$$;


